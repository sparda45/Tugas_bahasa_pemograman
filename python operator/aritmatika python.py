#aritmatika
x=10
y=5

print("x+y=", x+y) #penjumlahan
print("x-y=",x-y) #pengurangan
print("x*y=",x*y) #perkalian
print("x/y=",x/y) #pembagian(bilangan bulat)
print("x//y=",x//y)#pembagian bulat (sama saja hanya bilangan di belakang koma di hilangkan)
print("x%y=",x%y) #modulus(sisa pembagian)
print("x**y=",x**y) #perpangkatan